public class Calculator {
    public int sum(int x, int y) {return x + y; }

    public int subtractNum(int x, int y) { return x - y; }

    public int multiplyNum(int x, int y) { return x * y; }

    public int divideNum(int x, int y) { return x / y; }
}



//subtractNum  --> 'difference'
//multiplyNum  --> 'product'
//divideNum    --> 'quotient'